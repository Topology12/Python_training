set_a = set()

# Thêm "Anna" vào set
set_a.add("Anna")
print(set_a)

# Thêm một tuple ('Kenny', 'Jen', 'Danny')
set_a.update(('Kenny', 'Jen', 'Danny'))
print(set_a)

# In set_a và độ dài của nó
print(set_a)
print(len(set_a))

# Xóa "Jen" khỏi set_a
set_a.remove("Jen")
print(set_a)

# Xóa set_a
set_a.clear()
print(set_a)
